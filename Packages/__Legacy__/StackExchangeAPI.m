SESearch[
	site:_String?(MemberQ[Values@$StackExchangeSites,#]&)|Automatic:Automatic,
	objects:"Questions"|"Answers"|"Users"|"Q"|"A"|"U"|_List:"Questions",
	query:_String|None:None,
	ops:OptionsPattern[]]:=
	With[{obs=
		DeleteCases[Except[SEQuestions|SEAnswers|SEUsers]]@
		DeleteDuplicates@
			Replace[Flatten@{objects},{
				"Questions"|"Q"->SEQuestions,
				"Answers"|"A"->SEAnswers,
				"Users"|"U"->SEUsers
				},
				1]},
		Replace[
			Join@@
				DeleteCases[$Failed]@
				Table[
					Switch[ob,
						SEQuestions,
							SEQuestions[
								site,
								Sequence@@
								DeleteDuplicatesBy[
									{ops,
										"SortBy"->"votes",
										If[query=!=None,
											"FreeFormSearch"->query,
											Nothing
											]},
									First]
								],
						SEAnswers,
							SEAnswers[
								site,
								Sequence@@
								DeleteDuplicatesBy[
									{ops,
										If[query=!=None,
											"FreeFormSearch"->query,
											Nothing
											]},
									First]
								],
						SEUsers,
							SEUsers[
								site,
								Sequence@@
								DeleteDuplicatesBy[
									{ops,
										If[query=!=None,
											"UserName"->query,
											Nothing
											]},
									First]
								]
						],
					{ob,obs}
					],
				{
					{}->None,
					{r_}:>r
					}]
				
		]


